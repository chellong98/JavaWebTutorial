package Model;

import java.util.Date;

/**
 *
 * @author chellong
 */
public class SinhVien {
    private String id;
    private String name;
    private String lop;
    private Date dob;

    public SinhVien() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLop() {
        return lop;
    }

    public void setLop(String lop) {
        this.lop = lop;
    }

    public Date getDob() {
        return dob;
    }

    public void setDob(Date dob) {
        this.dob = dob;
    }

    @Override
    public String toString() {
        return "SinhVien{" + "id=" + id + ", name=" + name + ", lop=" + lop + ", dob=" + dob + '}';
    }
    
    
}
